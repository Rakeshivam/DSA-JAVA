

public class Binary {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 12;

        System.out.println("Add = "  + (a+b));
        System.out.println("Sub = "  + (a-b));
        System.out.println("Multiply = "  + (a*b));
        System.out.println("Divide = "  + (a/b));
        System.out.println("Modulo = "  + (a%b));
    }
}
