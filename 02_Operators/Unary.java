public class Unary {
    public static void main(String[] args) {
        
        int a = 10;
        int b = ++a;   //Pre increament

        System.out.println(a);
        System.out.println(b);

        int c = 10;
        int d = c++;     // Post increament

        System.out.println(c);
        System.out.println(d);
    }

}
