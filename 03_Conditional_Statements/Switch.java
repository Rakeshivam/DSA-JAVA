
public class Switch {
    public static void main(String[] args) {
        int num = 2;

        switch (num) {
            case 3:
                System.out.println("Samosa");
                break;
            case 1:
                System.out.println("Burger");
                break;
            case 2:
                System.out.println("Mango shake");
            default:
                System.out.println("We wake up");
                break;
        }
    }

}
